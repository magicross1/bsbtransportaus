// Helper functions for NSW data
export const getNSWCities = () => {
  return nswTransportZones.map(zone => zone.city).sort()
}

export const getNSWZones = () => {
  return [...new Set(nswTransportZones.map(zone => zone.zone))].sort()
}

export const getNSWZoneByCity = (city: string) => {
  return nswTransportZones.find(zone => zone.city.toLowerCase() === city.toLowerCase())
}

export const getNSWCitiesByZone = (zone: string) => {
  return nswTransportZones.filter(transportZone => transportZone.zone === zone).map(zone => zone.city)
}


